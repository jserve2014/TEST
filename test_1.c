void modify_str(char s[], int n, char c)
{
	int i = 0;
	char *p = s;
	while(*p!='\0')
	{
		if(i>n){
			*p = c;
		}
		p++;
		i++;
	}
}

char buf[]="abcd1234";
modify_str(buf,3 ,'a');
